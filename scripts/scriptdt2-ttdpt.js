const kyhoc = document.querySelectorAll(".btnkh");
var kyhientai = "ky1";
capnhatmonhoc(kyhientai);

kyhoc.forEach((buttons) => {
    buttons.addEventListener("click", () => {
        kyhoc.forEach((btnkh) => btnkh.classList.remove("active"));
        buttons.classList.add("active");
        kyhientai = buttons.getAttribute("data-kyhoc");
        capnhatmonhoc(kyhientai);
    });
});

function capnhatmonhoc(kyhoc) {
    const datamonhoc = curriculum[kyhoc];
    const monhientai = document.querySelectorAll(".monhoc");
    datamonhoc.forEach((mon, pos) => {
        const {
            ma,
            ten,
            loai,
            soTin
        } = mon;

        monhientai[pos].querySelector(".sotin").textContent = String(soTin) + " tín chỉ";
        monhientai[pos].querySelector(".tenmon").textContent = ten;
        monhientai[pos].querySelector(".mamon").textContent = ma;
        if (loai == 1)
            monhientai[pos].querySelector(".type").style.background = "#7DB1E3";
        if (loai == 2)
            monhientai[pos].querySelector(".type").style.background = "#75DEE3";
        if (loai == 3)
            monhientai[pos].querySelector(".type").style.background = "#E8923B";
        if (loai == 4)
            monhientai[pos].querySelector(".type").style.background = "#EA4444";
        if (loai == 5)
            monhientai[pos].querySelector(".type").style.background = "#2C51BE";
        if (loai == 6)
            monhientai[pos].querySelector(".type").style.background = "#FFA9A9";
        monhientai[pos].style.display = "block";
    });

    for (let i = datamonhoc.length; i < 12; i++) {
        monhientai[i].style.display = "none";
    }

}


const menuIcon = document.getElementById('menuIcon');
const popupMenu = document.getElementById('popupMenu');

menuIcon.addEventListener('click', () => {
    const isVisible = popupMenu.style.display === 'block';
    popupMenu.style.display = isVisible ? 'none' : 'block';
});

function navigateToSection(sectionId) {
    const section = document.getElementById(sectionId);
    section.scrollIntoView({
        behavior: 'smooth'
    });
    popupMenu.style.display = 'none'; 
}